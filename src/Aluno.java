import java.util.List;

public class Aluno {
	String nome;
	List<String> listaMateriasAprovadas;
	List<String> listaMateriasReprovadas;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public List<String> getListaMateriasAprovadas() {
		return listaMateriasAprovadas;
	}
	public void setListaMateriasAprovadas(List<String> listaMateriasAprovadas) {
		this.listaMateriasAprovadas = listaMateriasAprovadas;
	}
	public List<String> getListaMateriasReprovadas() {
		return listaMateriasReprovadas;
	}
	public void setListaMateriasReprovadas(List<String> listaMateriasReprovadas) {
		this.listaMateriasReprovadas = listaMateriasReprovadas;
	}
	
	
	
	
}
